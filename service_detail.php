<script>
    function ajaxFetch(routeName, formId, successDivId, errorDivId, loaderClass, showToastMsg, method, getRawResponse, callback) {
        showToastMsg = showToastMsg || true;
        method = method || "post";
        getRawResponse = getRawResponse || false;

        $.ajax({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            },
            type: method,
            url: routeName,
            data:  new FormData($(formId)[0]),
            beforeSend: function() {
                $(loaderClass).show();
            },
            cache: false,
            contentType: false,
            processData:false,
            success: function(response) {
                if(getRawResponse == true){
                    callback(response);
                }else{
                    if(response.code == 200){
                        if(response.type == 'error'){
                            if(showToastMsg == true){
                                toastr.error(response.message, 'Error', toastr.options);
                            }
                            $(errorDivId).show();
                            $(errorDivId).html(response.view);
                            $(successDivId).hide();
                        }else if(response.type == 'success'){
                            if(showToastMsg == true){
                                toastr.success(response.message, 'Success', toastr.options);
                            }
                            $(successDivId).show();
                            $(successDivId).html(response.view);
                            $(errorDivId).hide();
                        }
                    }else{
                        toastr.error('Something went wrong...!', 'Error', toastr.options);
                    }
                    $(loaderClass).hide();
                }
            }
        });
    }

</script>
